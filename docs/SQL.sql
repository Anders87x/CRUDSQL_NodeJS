CREATE DATABASE DBTEST2

USE DBTEST2

SELECT * FROM TM_CATEGORIA

CREATE PROCEDURE SP_I_CATEGORIA_01
@CAT_ID INT,
@CAT_NOM VARCHAR(50),
@CAT_OBS VARCHAR(150)
AS
BEGIN
	INSERT INTO TM_CATEGORIA
	(CAT_NOM,CAT_OBS)
	VALUES
	(@CAT_NOM,@CAT_OBS)

	SELECT * FROM TM_CATEGORIA
END

CREATE PROCEDURE SP_U_CATEGORIA_01
@CAT_ID INT,
@CAT_NOM VARCHAR(50),
@CAT_OBS VARCHAR(150)
AS
BEGIN
	UPDATE TM_CATEGORIA
	SET
		CAT_NOM = @CAT_NOM,
		CAT_OBS = @CAT_OBS
	WHERE 
		CAT_ID = @CAT_ID

	SELECT * FROM TM_CATEGORIA
END

